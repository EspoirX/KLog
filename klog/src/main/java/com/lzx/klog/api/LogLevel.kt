package com.lzx.klog.api

/**
 * Log level indicate how important the log is
 */
object LogLevel {

    /**
     * Log level for KLog.v
     */
    val LEVEL_VERBOSE = 1

    /**
     * Log level for KLog.d
     */
    val LEVEL_DEBUG = 2

    /**
     * Log level for KLog.i
     */
    val LEVEL_INFO = 3

    /**
     * Log level for KLog.w
     */
    val LEVEL_WARN = 4

    /**
     * Log level for KLog.e
     */
    val LEVEL_ERROR = 5
}